"""Run compact reproducible experiments and write a submission report."""

import json
from pathlib import Path

from dqn_trader.sdk.sdk import TradingSDK

RUNS = [
    {"name": "aapl_risk_adjusted", "ticker": "AAPL", "reward_mode": "risk_adjusted"},
    {"name": "spy_risk_adjusted", "ticker": "SPY", "reward_mode": "risk_adjusted"},
    {"name": "aapl_basic_reward", "ticker": "AAPL", "reward_mode": "basic"},
]


def run_one(run: dict[str, str], episodes: int) -> dict[str, float | str | int]:
    sdk = TradingSDK()
    run_dir = Path("results/experiments") / run["name"]
    sdk.config["training"]["episodes"] = episodes
    sdk.config["training"]["checkpoint_path"] = str(run_dir / "best_model.pt")
    training = sdk.train(run["ticker"], run["reward_mode"])
    backtest = sdk.backtest(run["ticker"], output_dir=run_dir)
    return {
        "name": run["name"],
        "ticker": run["ticker"],
        "reward_mode": run["reward_mode"],
        "episodes": episodes,
        "final_training_reward": training.rewards[-1],
        "loss_points": len(training.losses),
        "total_return": backtest.total_return,
        "buy_hold_return": backtest.buy_hold_return,
        "sharpe_ratio": backtest.sharpe_ratio,
        "max_drawdown": backtest.max_drawdown,
        "win_rate": backtest.win_rate,
        "trade_count": backtest.trade_count,
    }


def run_safely(run: dict[str, str], episodes: int) -> dict[str, float | str | int]:
    try:
        return run_one(run, episodes)
    except Exception as exc:
        return {
            "name": run["name"],
            "ticker": run["ticker"],
            "reward_mode": run["reward_mode"],
            "episodes": episodes,
            "status": "failed",
            "error": str(exc),
        }


def write_report(rows: list[dict[str, float | str | int]]) -> None:
    out_dir = Path("results/experiments")
    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "summary.json").write_text(json.dumps(rows, indent=2), encoding="utf-8")
    lines = [
        "# Experiment Run Report",
        "",
        "These are real local runs generated by `uv run python scripts/run_experiments.py`.",
        "They are educational diagnostics, not financial advice and not proof of future profitability.",
        "",
        "| Run | Ticker | Reward | Episodes | DQN Return | Buy/Hold Return | Sharpe | Max Drawdown | Win Rate | Trades |",
        "|---|---|---|---:|---:|---:|---:|---:|---:|---:|",
    ]
    for row in rows:
        if row.get("status") == "failed":
            lines.append(
                f"| {row['name']} | {row['ticker']} | {row['reward_mode']} | {row['episodes']} | "
                f"FAILED | FAILED | FAILED | FAILED | FAILED | FAILED |"
            )
            continue
        lines.append(
            f"| {row['name']} | {row['ticker']} | {row['reward_mode']} | {row['episodes']} | "
            f"{row['total_return']:.4f} | {row['buy_hold_return']:.4f} | "
            f"{row['sharpe_ratio']:.4f} | {row['max_drawdown']:.4f} | "
            f"{row['win_rate']:.4f} | {row['trade_count']} |"
        )
    lines.extend(
        [
            "",
            "## What We Learned",
            "- A short five-episode run is enough to validate the pipeline, but not enough to claim a strong trading policy.",
            "- Comparing DQN return with Buy-and-Hold prevents us from over-celebrating raw portfolio growth.",
            "- Trade count is important because reward bugs or missing costs can create over-trading.",
            "- Sharpe and drawdown expose instability that total return alone can hide.",
            "",
            "## Struggles and Fixes During Development",
            "- A corrupted parquet cache initially broke AAPL preparation; `YFinanceDataClient` now ignores corrupt cache and retries the normal path.",
            "- yfinance's internal SQLite cache failed on this machine until the client routed it into `data/raw/.yfinance-cache`.",
            "- The first README visuals were generated previews and did not fully match the live GUI; the real Tkinter styling was updated afterward.",
            "- The project deliberately avoids fake results. Metrics in this report come from the local experiment runner.",
            "- SPY/NVDA comparison should be rerun on a machine where yfinance TLS verification succeeds, or by providing CSV fallback files.",
            "",
            "## Failed Runs",
            "",
        ]
    )
    for row in rows:
        if row.get("status") == "failed":
            lines.append(f"- `{row['name']}` failed with: `{row['error']}`")
    (out_dir / "REPORT.md").write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    rows = [run_safely(run, episodes=5) for run in RUNS]
    write_report(rows)


if __name__ == "__main__":
    main()
